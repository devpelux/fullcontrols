namespace FullControls
{
    /// <summary>
    /// Type of <see cref="PolymorphicMenuItem"/>.
    /// </summary>
    public enum ItemType
    {
        /// <summary>
        /// The item is a normal item.
        /// </summary>
        Item,

        /// <summary>
        /// The item is a title (an unclickable text).
        /// </summary>
        Title,

        /// <summary>
        /// The item is a separator (an unclickable line).
        /// </summary>
        Separator,

        /// <summary>
        /// The item is a empty space.
        /// </summary>
        Space
    }
}
